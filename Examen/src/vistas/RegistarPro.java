    public boolean agregarPro(){
        try(Connection con = Conexion.conexion()) {
           String sql = "insert into productos(precio,tipo,nombre) "
                    + "values (?,?,?)";         
            PreparedStatement stmt = con.prepareStatement(sql); 
            stmt.setInt(1,Integer.parseInt(txtPrecio.getText()));
            stmt.setString(2, (String) cbxTipo.getSelectedItem());
            stmt.setString(3, txtNombre.getText());
            return stmt.executeUpdate() > 0;           
            
        } catch (SQLException s) {
            JOptionPane.showMessageDialog(null,"El producto ya existe");
            txtNombre.setText("");
            txtPrecio.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Problemas al cargar usuarios");
        }
        return false;
      
    }
